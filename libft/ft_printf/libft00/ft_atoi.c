#include "libft00.h"

int	ft_atoi(const char *nptr)
{
	unsigned int		i;
	int					sign;
	unsigned long long	fdb;

	i = 0;
	fdb = 0;
	sign = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		fdb = (fdb * 10) + (nptr[i] - 48);
		if (fdb > __LONG_MAX__ && sign == 1)
			return (-1);
		if (fdb > __LONG_MAX__ && sign == -1)
			return (0);
		i++;
	}
	return ((int)(fdb * sign));
}
